const runs = (text, opts = {}) => new TextRun({ text, font: FONT, size: SZ, ...opts });

// Обычный абзац (по ширине, отступ 1.25, интервал 1.5).
function P(text, opts = {}) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: LINE, before: 0, after: 0 },
    indent: { firstLine: FIRST },
    children: Array.isArray(text) ? text : [runs(text, opts)],
  });
}
// Абзац-перечисление с тире.
function dash(text) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: LINE },
    indent: { firstLine: FIRST },
    children: [runs("– " + text)],
  });
}
// Заголовок раздела (H1): по центру, прописными, с новой страницы.
function H1(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    alignment: AlignmentType.CENTER,
    pageBreakBefore: true,
    spacing: { line: LINE, before: 0, after: 240 },
    indent: { firstLine: 0 },
    children: [runs(text.toUpperCase(), { bold: true })],
  });
}
// Подзаголовок (H2): слева, полужирный.
function H2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    alignment: AlignmentType.LEFT,
    spacing: { line: LINE, before: 240, after: 120 },
    indent: { firstLine: 0 },
    children: [runs(text, { bold: true })],
  });
}
// Заголовок передней части (РЕФЕРАТ, СОДЕРЖАНИЕ) — не входит в оглавление.
function frontHeading(text, pageBreak = true) {
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    pageBreakBefore: pageBreak,
    spacing: { line: LINE, before: 0, after: 240 },
    indent: { firstLine: 0 },
    children: [runs(text.toUpperCase(), { bold: true })],
  });
}
function center(text, opts = {}) {
  return new Paragraph({
    alignment: AlignmentType.CENTER, spacing: { line: LINE }, indent: { firstLine: 0 },
    children: [runs(text, opts)],
  });
}
function empty() { return new Paragraph({ children: [runs("")], spacing: { line: LINE } }); }

// Рисунок с подписью.
function figure(file, dir, caption, maxWcm = 16.5, maxHcm = 22) {
  const buf = fs.readFileSync(path.join(dir, file));
  const w = buf.readUInt32BE(16), h = buf.readUInt32BE(20);
  const ratio = h / w;
  let wcm = maxWcm;
  if (wcm * ratio > maxHcm) wcm = maxHcm / ratio;
  const wpx = Math.round((wcm / 2.54) * 96);
  const hpx = Math.round(wpx * ratio);
  return [
    new Paragraph({
      alignment: AlignmentType.CENTER, spacing: { line: LINE, before: 120 }, indent: { firstLine: 0 },
      children: [new ImageRun({ type: "png", data: buf, transformation: { width: wpx, height: hpx } })],
    }),
    new Paragraph({
      alignment: AlignmentType.CENTER, spacing: { line: LINE, after: 120 }, indent: { firstLine: 0 },
      children: [runs(caption)],
    }),
  ];
}
// Заголовок таблицы (слева, над таблицей).
function tableTitle(text) {
  return new Paragraph({
    alignment: AlignmentType.LEFT, spacing: { line: LINE, before: 240, after: 60 }, indent: { firstLine: 0 },
    children: [runs(text)],
  });
}
// Заголовок листинга.
function listingTitle(text) {
  return new Paragraph({
    alignment: AlignmentType.LEFT, spacing: { line: LINE, before: 120, after: 60 }, indent: { firstLine: 0 },
    children: [runs(text)],
  });
}
// Листинг кода (Courier New 11, одинарный интервал).
function listing(lines) {
  return lines.map((ln) => new Paragraph({
    spacing: { line: 240, before: 0, after: 0 }, indent: { firstLine: 0, left: 283 },
    children: [new TextRun({ text: ln === "" ? " " : ln, font: "Courier New", size: 22 })],
  }));
}
// Ячейка таблицы.
function tc(text, { w, header = false, bold = false, align = AlignmentType.LEFT } = {}) {
  const border = { style: BorderStyle.SINGLE, size: 4, color: "000000" };
  return new TableCell({
    width: { size: w, type: WidthType.DXA },
    margins: { top: 40, bottom: 40, left: 90, right: 90 },
    verticalAlign: VerticalAlign.CENTER,
    borders: { top: border, bottom: border, left: border, right: border },
    shading: header ? { fill: "E7E7E7", type: ShadingType.CLEAR, color: "auto" } : undefined,
    children: [new Paragraph({
      alignment: align, spacing: { line: LINE }, indent: { firstLine: 0 },
      children: [runs(text, { bold: header || bold })],
    })],
  });
}
function table(widths, rows) {
  return new Table({
    width: { size: widths.reduce((a, b) => a + b, 0), type: WidthType.DXA },
    columnWidths: widths,
    rows: rows.map((cells, ri) => new TableRow({
      tableHeader: ri === 0,
      children: cells.map((c, ci) => tc(c.t, { w: widths[ci], header: ri === 0, align: c.a, bold: c.b })),
    })),
  });
}

